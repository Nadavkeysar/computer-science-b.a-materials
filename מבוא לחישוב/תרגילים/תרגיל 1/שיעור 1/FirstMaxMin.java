
public class FirstMaxMin {

	public static void main(String[] args) {
		int x = Integer.MAX_VALUE;
		System.out.println("Integer.MAX_VALUE = "+x);
		int y = x+1;
		System.out.println("y = "+y);
		System.out.println("Integer.MIN_VALUE = "+Integer.MIN_VALUE);
		int Y = 80;
		System.out.println("Y = "+Y);
	}
}
